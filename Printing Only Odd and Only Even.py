# while loop

# Only Odd
i = 1
while i<=50:
    if i%2 != 0:
        print(i)
    i += 1

#Only Even
i = 1
while i<=50:
    if i%2 == 0:
        print(i)
    i += 1

#for loop

#Only Odd
for i in range(1,21):
    if i%2 != 0:
        print(i)

#Only Even
for i in range(1,21):
    if i%2 == 0:
        print(i)